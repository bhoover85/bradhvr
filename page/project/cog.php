<p>
Casualties of the Gridiron is a documentary series by Isaac Solotaroff that was aired by <a href="http://www.gq.com/">GQ</a>. It follows a group of retired NFL players battling chronic pain, drug addiction and brain trauma. The series chronicles their journey back to health and redemption through the support of one another and a group of dedicated medical professionals.
</p>

<p>
<a href="https://twitter.com/renomsad">Reno Msad</a> did the design for Casualties of the Gridiron. I exported the design assets from Photoshop and was responsible for all code. The site is a one page design using HTML5 Boilerplate, CSS, PHP and jQuery. The videos are on YouTube and I embedded them as they became available.
</p>

<p>
Due to the time sensitive nature for the premiere of the first episode I had to build the site over the span of a weekend. This was a challenge, but I enjoyed working on this project with Reno and Isaac.
</p>