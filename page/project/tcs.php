<p>
Thin Crust Square is a promotional page for Emma's Brick Oven Pizza in Cranford, NJ.
</p>

<p>
<a href="http://www.x4dc.com/">X4DC</a> did the design for Thin Crust Square. I exported the design assets from Photoshop and was responsible for all code. The site is a multi page design using HTML5 Boilerplate, CSS, PHP and jQuery.
</p>

<p>
TCS was a fairly simple and straightforward project. Emma's Brick Oven wanted an embedded Instagram feed and photo gallery. I implemented this using the Instagram API, PHP and a jQuery lightbox.
</p>